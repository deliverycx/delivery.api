const trueDate = new Date();
function formatDate(date: any) {
	var dd: any = date.getDate();
	if (dd < 10) dd = '0' + dd;

	var mm: any = date.getMonth() + 1;
	if (mm < 10) mm = '0' + mm;

	var yy: any = date.getFullYear() % 100;
	if (yy < 10) yy = '0' + yy;

	return dd + '.' + mm + '.' + yy;
}
